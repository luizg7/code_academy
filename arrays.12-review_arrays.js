/*

.Arrays are lists that store data in JavaScript.

.Arrays are create with brackets [].

.Each item inside of an array is a at a number position, or index, starting at 0.

.We can access one item in an array using its index, with syntax like: myArray[0].

.Arrays have a length property, which allows you to see how many items are in an array.

.Arrays have their own methods, including .push() and .pop(), which add and remove items from an array, respectively.

.Arrays have many methods that perform different tasks, such as .slice() and .shift()

.Some built-in methods are mutating, meaning the method will change the array, while others are not mutating. You always check the documentation.

.Variables that contain arrays can be declared with let or const. Even when declared with const, arrays are still mutable. However, a variable declared with const cannot be reassing.

.Arrays mutated inside of a function will keep that change even outside the function.

.Arrays can be nested inside other arrays.

.To access elements in nested arrays chain indices using bracket notation.

*/
